export const authConfig = {
  pages: {
    signIn: "/profile", 
  },
  callbacks: {
    // Modify JWT token
    async jwt({ token, user }) {
      if (user) {
        token.id = user.id;
        token.isAdmin = user.isAdmin || false;
      }
      return token;
    },
    // Pass user information to session
    async session({ session, token }) {
      if (token) {
        session.user.id = token.id;
        session.user.isAdmin = token.isAdmin;
      }
      return session;
    },
  },
};
